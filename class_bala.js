class Bala {
  constructor(x, y) {
    this.x = x;
    this.y = y;
    this.velocidad = 7;
    this.ancho = 5;
    this.alto = 10;
  }

  mover() {
    this.y -= this.velocidad;
  }

  mostrar() {
    fill(255, 0, 0);
    rect(this.x, this.y, this.ancho, this.alto);
  }

  fueraDePantalla() {
    return this.y < 0;
  }
}
